public class cicloswP65 {
    public static void main(String[] args) {
        //conteo de 0 a 20;
        int conta = 50;
        while (conta<=20){
            System.out.println(conta);
            conta ++;
        }
        System.out.println("------------------------------------------------");
        conta = 50;
        do{
            System.out.println(conta);
            conta ++;
        }while(conta<=20);
    }
}
